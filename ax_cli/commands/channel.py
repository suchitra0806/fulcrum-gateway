    async def handle_tool_call(self, request_id: Any, params: dict[str, Any]) -> None:
        name = params.get("name")
        arguments = params.get("arguments") or {}
        if name == "get_messages":
            await self.handle_get_messages(request_id, arguments)
            return
        if name != "reply":
            await self.send_error(request_id, -32601, f"Unknown tool: {name}")
            return

        text = str(arguments.get("text") or "").strip()
        reply_to = arguments.get("reply_to") or self._last_message_id
        if not text:
            await self.send_error(request_id, -32602, "reply.text is required")
            return
        if not reply_to:
            await self.send_error(request_id, -32602, "reply_to is required until at least one aX message has arrived")
            return
        if getattr(self.client, "_use_exchange", False) and not str(getattr(self.client, "token", "")).startswith(
            "axp_a_"
        ):
            await self.send_response(
                request_id,
                {
                    "content": [
                        {
                            "type": "text",
                            "text": (
                                "reply failed: ax channel requires an agent-bound PAT (axp_a_). "
                                "User PATs may create agent PATs but cannot speak as an agent."
                            ),
                        }
                    ],
                    "isError": True,
                },
            )
            return
        if not self.agent_id:
            await self.send_response(
                request_id,
                {
                    "content": [
                        {
                            "type": "text",
                            "text": "reply failed: channel agent_id is required for agent runtime replies.",
                        }
                    ],
                    "isError": True,
                },
            )
            return

        try:

            def _send_as_agent():
                """Send using the agent_access JWT produced by the agent-bound PAT."""
                metadata = merge_explicit_mentions_metadata(
                    {"top_level_ingress": False, "routing": {"mode": "reply_target", "source": "channel_reply"}},
                    text,
                    exclude=[self.agent_name],
                )
                return self.client.send_message(self.space_id, text, parent_id=reply_to, metadata=metadata)

            data = await asyncio.to_thread(_send_as_agent)
            message = unwrap_envelope(data, "message")
            sent_id = message.get("id") or (data.get("id") if isinstance(data, dict) else None)
            _remember_reply_anchor(self._reply_anchor_ids, sent_id)
            await self.publish_processing_status(reply_to, "completed")
            await self.touch_gateway(
                "channel_reply_sent",
                message_id=reply_to,
                reply_id=sent_id,
                reply_preview=text[:160],
                current_status=None,
                current_activity=None,
                last_reply_message_id=sent_id,
                last_reply_preview=text[:240],
                last_work_completed_at=time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
                backlog_depth=self.mention_queue.qsize(),
            )

            # Bundle "what arrived while you were drafting" into the response.
            # Same intent as `_send_from_managed_agent`'s inbox bundling for
            # the CLI agents-send path: closes aX task 663d9e6f. Default ON;
            # the caller can pass {"inbox": false} to skip.
            include_inbox = arguments.get("inbox") is not False
            inbox_bundle = None
            inbox_error = None
            if include_inbox and self.agent_id:
                try:
                    inbox_bundle = await self._poll_channel_inbox_after_reply(
                        wait_seconds=int(arguments.get("inbox_wait", 2) or 0),
                        limit=int(arguments.get("inbox_limit", 10) or 10),
                    )
                except Exception as exc:
                    inbox_error = str(exc)

            response_content: list[dict[str, Any]] = [
                {
                    "type": "text",
                    "text": f"sent reply to {reply_to}" + (f" ({sent_id})" if sent_id else ""),
                }
            ]
            if inbox_bundle and inbox_bundle.get("messages"):
                response_content.append(
                    {
                        "type": "text",
                        "text": _format_inbox_bundle_for_mcp(inbox_bundle),
                    }
                )
            elif inbox_error:
                response_content.append({"type": "text", "text": f"(inbox poll failed: {inbox_error})"})

            await self.send_response(request_id, {"content": response_content})
            self.log(f"replied to {reply_to}")
        except Exception as exc:  # pragma: no cover - exercised in live runs
            await self.send_response(
                request_id,
                {
                    "content": [{"type": "text", "text": f"reply failed: {exc}"}],
                    "isError": True,
                },
            )

    async def _poll_channel_inbox_after_reply(
        self,
        *,
        wait_seconds: int,
        limit: int,
    ) -> dict[str, Any]:
        """After a channel reply lands, return any unread messages addressed
        to this agent so the caller doesn't talk past whatever arrived while
        the reply was being composed.

        Mirrors the wait-loop shape of ``_poll_managed_agent_inbox_after_send``
        in ``commands/gateway.py``: poll once immediately, then keep polling
        on ``poll_interval`` until messages arrive or the deadline elapses.
        Uses the channel's already-authenticated agent client so no extra
        identity setup is needed.
        """
        deadline = time.monotonic() + max(0, int(wait_seconds))
        capped_limit = max(1, min(int(limit), 100))
        poll_interval = 1.0
        while True:
            data = await asyncio.to_thread(
                self.client.list_messages,
                limit=capped_limit,
                space_id=self.space_id,
                agent_id=self.agent_id,
                unread_only=True,
                mark_read=True,
                channel="main",
            )
            messages = (
                data if isinstance(data, list) else (data.get("messages") if isinstance(data, dict) else []) or []
            )
            unread_count = (
                data.get("unread_count") if isinstance(data, dict) and "unread_count" in data else len(messages)
            )
            bundle: dict[str, Any] = {
                "agent": self.agent_name,
                "agent_id": self.agent_id,
                "space_id": self.space_id,
                "messages": messages,
                "unread_count": unread_count,
            }
            if messages or wait_seconds <= 0 or time.monotonic() >= deadline:
                return bundle
            await asyncio.sleep(poll_interval)

    async def handle_request(self, request: dict[str, Any]) -> None:
        request_id = request.get("id")
        method = request.get("method")
        params = request.get("params") or {}

        if method == "initialize":
            await self.handle_initialize(request_id)
        elif method == "tools/list":
            await self.handle_tools_list(request_id)
        elif method == "resources/list":
            await self.handle_empty_list(request_id, "resources")
        elif method == "resources/templates/list":
            await self.handle_empty_list(request_id, "resourceTemplates")
        elif method == "prompts/list":
            await self.handle_empty_list(request_id, "prompts")
        elif method == "tools/call":
            await self.handle_tool_call(request_id, params)
        elif method == "ping":
            await self.touch_gateway("channel_ping", current_status=None, current_activity=None)
            await self.send_response(request_id, {})
        else:
            await self.send_error(request_id, -32601, f"Method not found: {method}")

