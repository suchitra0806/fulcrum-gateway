def _default_channel_env_path() -> Path:
    override = os.environ.get("AX_CHANNEL_ENV_FILE")
    if override and override.strip():
        return Path(override).expanduser()
    return CHANNEL_ENV_PATH


def _load_channel_env(path: Path | None = None) -> None:
    """Load KEY=VALUE channel env defaults without overriding real env vars."""
    path = path or _default_channel_env_path()
    if not path.exists():
        return
    for line in path.read_text().splitlines():
        raw = line.strip()
        if not raw or raw.startswith("#") or "=" not in raw:
            continue
        key, value = raw.split("=", 1)
        key = key.strip()
        if not key or key in os.environ:
            continue
        os.environ[key] = value.strip().strip("\"'")


def _env_line(key: str, value: str) -> str:
    cleaned = str(value or "").replace("\n", " ").strip()
    escaped = cleaned.replace("\\", "\\\\").replace('"', '\\"')
    return f'{key}="{escaped}"'


def _write_channel_env(path: Path, values: dict[str, str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "# Generated by ax channel setup. Do not put user PATs here.",
        *[_env_line(key, value) for key, value in values.items() if str(value or "").strip()],
        "",
    ]
    path.write_text("\n".join(lines))
    path.chmod(0o600)


def _load_mcp_config(path: Path) -> dict[str, Any]:
    if not path.exists():
        return {}
    try:
        loaded = json.loads(path.read_text())
    except json.JSONDecodeError as exc:
        raise typer.BadParameter(f"{path} is not valid JSON: {exc}") from exc
    if not isinstance(loaded, dict):
        raise typer.BadParameter(f"{path} must contain a JSON object.")
    return loaded


def _write_mcp_server_config(path: Path, server_name: str, server_config: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    config = _load_mcp_config(path)
    servers = config.setdefault("mcpServers", {})
    if not isinstance(servers, dict):
        raise typer.BadParameter(f"{path} has a non-object mcpServers value.")
    servers[server_name] = server_config
    path.write_text(json.dumps(config, indent=2, sort_keys=True) + "\n")


def _gateway_agent_channel_defaults(agent_name: str) -> dict[str, str]:
    try:
        registry = gateway_core.load_gateway_registry()
    except Exception:
        return {}
    entry = gateway_core.find_agent_entry(registry, agent_name)
    if not entry:
        return {}
    return {
        "AX_TOKEN_FILE": str(entry.get("token_file") or ""),
        "AX_BASE_URL": str(entry.get("base_url") or ""),
        "AX_AGENT_NAME": str(entry.get("name") or agent_name),
        "AX_AGENT_ID": str(entry.get("agent_id") or ""),
        "AX_SPACE_ID": str(entry.get("space_id") or ""),
        "AX_GATEWAY_WORKDIR": str(entry.get("workdir") or ""),
        "model": str(entry.get("model") or ""),
    }


