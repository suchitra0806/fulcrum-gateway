def _default_local_channel_command() -> str:
    """Resolve the axctl path written into a generated `.mcp.json`.

    Claude Code launches the channel server from inside the agent's workdir,
    which is almost never the same directory the operator was in when they
    ran `ax channel setup`. Anything relative (e.g., `.venv/bin/axctl` from a
    cwd-anchored launcher) breaks at launch time because the agent workdir
    has no `.venv/`. Resolve to absolute before writing it out.

    Order of preference:

    1. Sibling `axctl` next to the running launcher (covers editable/dev
       installs where `ax` and `axctl` live together in `.venv/bin`).
    2. `shutil.which("axctl")` — system PATH lookup, what a fresh shell
       would resolve.
    3. Bare `"axctl"` so MCP can still try PATH at launch time.
    """
    raw = sys.argv[0] if sys.argv else ""
    if raw:
        launcher = Path(raw).expanduser().resolve()
        if launcher.name in {"ax", "axctl"}:
            sibling = launcher.with_name("axctl")
            if sibling.exists():
                return str(sibling)
    import shutil

    found = shutil.which("axctl")
    if found:
        return str(Path(found).resolve())
    return "axctl"


def _touch_gateway_channel_entry(
    agent_name: str,
    *,
    event: str | None = None,
    message_id: str | None = None,
    reply_id: str | None = None,
    reply_preview: str | None = None,
    **updates: Any,
) -> None:
    """Best-effort local Gateway heartbeat for attached channel sessions."""
    try:
        registry = gateway_core.load_gateway_registry()
        entry = gateway_core.find_agent_entry(registry, agent_name)
        if not entry:
            return
        now = time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime())
        entry.update(
            {
                "effective_state": "running",
                "last_seen_at": now,
                "backlog_depth": 0,
                "last_channel_heartbeat_at": now,
                **updates,
            }
        )
        gateway_core.save_gateway_registry(registry)
        if event:
            gateway_core.record_gateway_activity(
                event,
                entry=entry,
                message_id=message_id,
                reply_id=reply_id,
                reply_preview=reply_preview,
            )
    except Exception:
        return


def _channel_mcp_server_config(
    *,
    agent_name: str,
    space_id: str,
    env_path: Path,
    mode: str,
    container_image: str | None,
    debug: bool,
) -> dict[str, Any]:
    # Keep .mcp.json dry: identity, space, token, and base URL live in the
    # generated env file, while the project-local .ax/config.toml gives CLI
    # tools the same Gateway identity from inside the workspace.
    args = ["channel"]
    if debug:
        args.append("--debug")
    if mode == "local":
        return {
            "type": "stdio",
            "command": _default_local_channel_command(),
            "args": args,
            "env": {"AX_CHANNEL_ENV_FILE": str(env_path)},
        }
    image = str(container_image or "").strip()
    if not image:
        raise typer.BadParameter("--container-image is required when --mode docker.")
    container_env_path = "/run/ax-channel/.env"
    return {
        "type": "stdio",
        "command": "docker",
        "args": [
            "run",
            "--rm",
            "-i",
            "-e",
            f"AX_CHANNEL_ENV_FILE={container_env_path}",
            "-v",
            f"{env_path}:{container_env_path}:ro",
            image,
            "axctl",
            *args,
        ],
    }


def write_channel_setup(
    *,
    agent_name: str,
    workdir: Path,
    client: str = "claude_cli",
    space_id: str | None = None,
    base_url: str | None = None,
    token_file: Optional[Path] = None,
    agent_id: str | None = None,
    server_name: str = "ax-channel",
    env_path: Optional[Path] = None,
    mode: str = "local",
    container_image: str | None = None,
    debug: bool = False,
) -> dict[str, Any]:
    """Write MCP channel config for the given client and return launch details."""
    agent_name = agent_name.strip()
    if not agent_name:
        raise typer.BadParameter("Agent name is required.")
    normalized_mode = mode.strip().lower()
    if normalized_mode not in {"local", "docker"}:
        raise typer.BadParameter("--mode must be local or docker.")
    client_profile = _resolve_client_profile(client)

    defaults = _gateway_agent_channel_defaults(agent_name)
    resolved_space_id = str(space_id or defaults.get("AX_SPACE_ID") or "").strip()
    if not resolved_space_id:
        raise typer.BadParameter("--space-id is required when the agent is not present in the Gateway registry.")
    resolved_base_url = str(base_url or defaults.get("AX_BASE_URL") or "https://paxai.app").strip()
    resolved_token_file = token_file or (
        Path(defaults["AX_TOKEN_FILE"]).expanduser() if defaults.get("AX_TOKEN_FILE") else None
    )
    if not resolved_token_file:
        raise typer.BadParameter("--token-file is required when the agent is not present in the Gateway registry.")
    resolved_agent_id = str(agent_id or defaults.get("AX_AGENT_ID") or "").strip()

    target_workdir = workdir.expanduser().resolve()
    mcp_path = target_workdir / ".mcp.json"
    resolved_env_path = env_path.expanduser() if env_path else client_profile.env_dir / f"{agent_name}.env"
    env_values = {
        "AX_TOKEN_FILE": str(resolved_token_file.expanduser()),
        "AX_BASE_URL": resolved_base_url,
        "AX_AGENT_NAME": agent_name,
        "AX_AGENT_ID": resolved_agent_id,
        "AX_SPACE_ID": resolved_space_id,
    }
    if os.environ.get("AX_OFFLINE"):
        env_values["AX_OFFLINE"] = "1"
    server_config = _channel_mcp_server_config(
        agent_name=agent_name,
        space_id=resolved_space_id,
        env_path=resolved_env_path,
        mode=normalized_mode,
        container_image=container_image,
        debug=debug,
    )
    _write_channel_env(resolved_env_path, env_values)
    _write_mcp_server_config(mcp_path, server_name, server_config)
    cli_config_path = target_workdir / ".ax" / "config.toml"
    _write_gateway_cli_config(
        cli_config_path,
        agent_name=agent_name,
        base_url="http://127.0.0.1:8765",
        workdir=target_workdir,
    )
    cli_readme_path = target_workdir / ".ax" / "README.md"
    _write_channel_workspace_readme(cli_readme_path, agent_name=agent_name, workdir=target_workdir)
    context_path = _write_channel_workspace_context(target_workdir, agent_name=agent_name)

    registered_model = str(defaults.get("model") or "").strip() or None
    write_model_to_settings(target_workdir, "claude_cli", registered_model)

    launch_command = client_profile.launch_template.format(
        mcp_path=mcp_path,
        server_name=server_name,
    )
    return {
        "agent": agent_name,
        "client": client.strip().lower(),
        "space_id": resolved_space_id,
        "base_url": resolved_base_url,
        "mode": normalized_mode,
        "model": registered_model,
        "mcp_path": str(mcp_path),
        "env_path": str(resolved_env_path),
        "cli_config_path": str(cli_config_path),
        "cli_readme_path": str(cli_readme_path),
        "agent_context_path": str(context_path),
        "server_name": server_name,
        "launch_command": launch_command,
    }


@dataclass(slots=True)
class MentionEvent:
    message_id: str
    parent_id: str | None
    conversation_id: str | None
    author: str
    prompt: str
    raw_content: str
    created_at: str | None
    space_id: str
    attachments: list[dict[str, Any]] | None = None
    metadata: dict[str, Any] | None = None


def _string_value(value: Any) -> str | None:
    if value is None:
        return None
    if isinstance(value, (str, int, float)):
        text = str(value).strip()
        return text or None
    return None


def _format_shared_object(metadata: dict[str, Any] | None, *, space_id: str) -> str | None:
    if not isinstance(metadata, dict):
        return None
    forward = metadata.get("forward")
    if not isinstance(forward, dict):
        return None

    fields = [
        ("resource_type", "resource_type"),
        ("resource_id", "resource_id"),
        ("task_id", "task_id"),
        ("context_key", "context_key"),
        ("resource_uri", "resource_uri"),
        ("source_message_id", "source_message_id"),
        ("source_card_id", "source_card_id"),
        ("title", "title"),
    ]
    lines = ["Shared object:"]
    for label, key in fields:
        value = _string_value(forward.get(key))
        if value:
            lines.append(f"- {label}: {value}")

    summary = _string_value(forward.get("summary"))
    if summary:
        lines.append(f"- summary: {summary}")

    task_id = _string_value(forward.get("task_id"))
    context_key = _string_value(forward.get("context_key"))
    if task_id or context_key:
        lines.append("")
        lines.append("Suggested inspection:")
        if task_id:
            lines.append(f"- axctl tasks get {task_id} --space-id {space_id} --json")
        if context_key:
            lines.append(f"- axctl context get '{context_key}' --space-id {space_id} --json")

    return "\n".join(lines) if len(lines) > 1 else None


def _format_attachments(attachments: list[dict[str, Any]] | None, *, space_id: str) -> str | None:
    if not attachments:
        return None
    lines = ["Attachments:"]
    context_keys: list[str] = []
    for attachment in attachments:
        if not isinstance(attachment, dict):
            continue
        filename = _string_value(attachment.get("filename")) or "attachment"
        content_type = _string_value(attachment.get("content_type"))
        attachment_id = _string_value(attachment.get("id") or attachment.get("attachment_id"))
        context_key = _string_value(attachment.get("context_key") or attachment.get("key"))
        details = [part for part in (content_type, f"id={attachment_id}" if attachment_id else None) if part]
        if context_key:
            details.append(f"context_key={context_key}")
            context_keys.append(context_key)
        lines.append(f"- {filename}" + (f" ({', '.join(details)})" if details else ""))
    if context_keys:
        lines.append("")
        lines.append("Suggested attachment inspection:")
        for key in context_keys:
            lines.append(f"- axctl context get '{key}' --space-id {space_id} --json")
    return "\n".join(lines) if len(lines) > 1 else None


def _enrich_prompt_for_agent(
    prompt: str,
    *,
    metadata: dict[str, Any] | None,
    attachments: list[dict[str, Any]] | None,
    space_id: str,
) -> str:
    blocks = [
        block
        for block in (
            _format_shared_object(metadata, space_id=space_id),
            _format_attachments(attachments, space_id=space_id),
        )
        if block
    ]
    if not blocks:
        return prompt
    return prompt.rstrip() + "\n\n---\n" + "\n\n".join(blocks)


def _format_inbox_bundle_for_mcp(bundle: dict[str, Any]) -> str:
    """Render an inbox bundle (from `_poll_channel_inbox_after_reply`) as a
    human-readable second text item for an MCP tool response.

    Shape matches what an LLM caller can act on directly: a one-line summary
    of the unread count, then per-message lines with sender + first-line
    preview. Capped at 5 messages in the body to keep the response compact —
    the full list is still in the bundle if a richer client wants it.
    """
    messages = bundle.get("messages") or []
    unread_count = bundle.get("unread_count") or len(messages)
    lines = [
        f"INBOX while you were drafting: {unread_count} unread message(s) addressed to @{bundle.get('agent') or 'this agent'}.",
    ]
    for msg in messages[:5]:
        if not isinstance(msg, dict):
            continue
        sender = msg.get("agent_name") or msg.get("user_name") or msg.get("sender") or "unknown"
        msg_id = msg.get("id") or msg.get("message_id") or ""
        first_line = ""
        content = msg.get("content")
        if isinstance(content, str) and content.strip():
            first_line = content.strip().splitlines()[0][:160]
        suffix = f" ({msg_id})" if msg_id else ""
        lines.append(f"  - @{sender}{suffix}: {first_line}")
    if len(messages) > 5:
        lines.append(f"  ... and {len(messages) - 5} more (full list in bundle).")
    return "\n".join(lines)


class ChannelBridge:
    def __init__(
        self,
        *,
        client,
        agent_name: str,
        agent_id: str | None,
        space_id: str,
        queue_size: int,
        debug: bool,
        processing_status: bool,
    ) -> None:
        self.client = client
        self.agent_name = agent_name
        self.agent_id = agent_id
        self.space_id = space_id
        self.debug = debug
        self.processing_status = processing_status
        self.loop: asyncio.AbstractEventLoop | None = None
        self.mention_queue: asyncio.Queue[MentionEvent] = asyncio.Queue(maxsize=queue_size)
        self.initialized = asyncio.Event()
        self.shutdown = threading.Event()
        self._stderr_lock = threading.Lock()
        self._write_lock = asyncio.Lock()
        self._last_message_id: str | None = None
        self._reply_anchor_ids: set[str] = set()
        self._pending_mentions: list[MentionEvent] = []

    def log(self, message: str) -> None:
        if not self.debug:
            return
        with self._stderr_lock:
            print(f"[{SERVER_NAME}] {message}", file=sys.stderr, flush=True)

    def enqueue_from_thread(self, event: MentionEvent) -> None:
        if not self.loop or self.shutdown.is_set():
            self.log(f"enqueue_from_thread: dropped (loop={self.loop is not None}, shutdown={self.shutdown.is_set()})")
            return

        def _push() -> None:
            try:
                self.mention_queue.put_nowait(event)
                qsize = self.mention_queue.qsize()
                self.log(f"enqueue: queued {event.message_id[:12]} (qsize={qsize})")
                asyncio.ensure_future(self._signal_received(event.message_id, qsize))
            except asyncio.QueueFull:
                self.log(f"queue full — dropping mention {event.message_id}")

        self.loop.call_soon_threadsafe(_push)

    async def _signal_received(self, message_id: str, queue_depth: int) -> None:
        """Emit 'received' status as soon as the SSE event is enqueued.

        Fires before emit_mentions picks up the event, closing the signal
        gap between message arrival and agent work start.
        """
        await self.publish_processing_status(message_id, "received")
        await self.touch_gateway(
            "channel_message_received",
            message_id=message_id,
            backlog_depth=queue_depth,
        )

    async def write_message(self, payload: dict[str, Any]) -> None:
        async with self._write_lock:
            raw = json.dumps(payload, separators=(",", ":")) + "\n"
            sys.stdout.write(raw)
            sys.stdout.flush()
            method = payload.get("method", payload.get("id", "?"))
            self.log(f"wrote to stdout: {method} ({len(raw)} bytes)")

    async def send_notification(self, method: str, params: dict[str, Any]) -> None:
        await self.write_message({"jsonrpc": "2.0", "method": method, "params": params})

    async def publish_processing_status(self, message_id: str, status: str) -> None:
        """Best-effort Activity Stream signal for channel delivery/progress.

        This lets the frontend show the same inline "agent is working" affordance
        for Claude Code channel sessions that it shows for other agent runtimes.
        It is intentionally non-blocking: channel delivery/replies must still
        work if the progress endpoint is unavailable.
        """
        if not self.processing_status:
            return
        try:

            def _send_status():
                return self.client.set_agent_processing_status(
                    message_id,
                    status,
                    agent_name=self.agent_name,
                    space_id=self.space_id,
                )

            await asyncio.to_thread(_send_status)
            self.log(f"processing status {status} for {message_id[:12]}")
        except Exception as exc:  # pragma: no cover - live best-effort path
            self.log(f"processing status failed for {message_id[:12]}: {exc}")

